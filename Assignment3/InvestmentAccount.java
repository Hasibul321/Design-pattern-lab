// Investment Account Module
class InvestmentAccount {
    public void checkBalance() {
        System.out.println("\n Investment Account: Checking balance...");
    }

    public void invest(int amount) {
        System.out.println("Investment Account: Investing $" + amount);
    }

    public void liquidate(int amount) {
        System.out.println("Investment Account: Liquidating $" + amount);
    }

}
