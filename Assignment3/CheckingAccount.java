// Checking Account Module
class CheckingAccount {
    public void checkBalance() {
        System.out.println("\n Checking Account: Checking balance...");
    }

    public void deposit(int amount) {
        System.out.println("Checking Account: Depositing $" + amount);
    }

    public void withdraw(int amount) {
        System.out.println("Checking Account: Withdrawing $" + amount);
    }
}
